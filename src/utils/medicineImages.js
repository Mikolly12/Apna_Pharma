/**
 * Get image URL for a medicine
 * @param {string} medicineName - The name of the medicine
 * @returns {string} Image URL or placeholder
 */
export const getMedicineImage = (medicineName) => {
  if (!medicineName) {
    return "https://via.placeholder.com/400x400/e0e0e0/999?text=Medicine";
  }
  
  // Return specific medicine image or placeholder
  return (
    MEDICINE_IMAGES[medicineName] || 
    `https://via.placeholder.com/400x400/e0e0e0/999?text=${encodeURIComponent(medicineName.slice(0, 15))}`
  );
};
